def analyze_text_metadata(description: str, notes: str) -> tuple:
    """Enhanced emotion detection with confidence scoring"""
    combined_text = f"{notes} {description}"
    blob = TextBlob(combined_text)
    polarity = blob.sentiment.polarity
    subjectivity = blob.sentiment.subjectivity

    # Enhanced emotion detection
    if polarity < -0.3:
        emotion = "Stressed/Overwhelmed"
    elif polarity < -0.1:
        emotion = "Uncertain/Anxious"
    elif polarity > 0.3:
        emotion = "Confident/Excited"
    elif polarity > 0.1:
        emotion = "Optimistic"
    else:
        emotion = "Neutral/Focused"

    # Get skill gaps with fallback chain
    skill_gaps = detect_skill_gaps_llm(description, notes)
    if not skill_gaps:  # If LLM fails, use contextual fallback
        skill_gaps = get_contextual_fallback(description, notes)
        print(f"🔄 Using contextual fallback for skill gaps: {skill_gaps}")

    return emotion, skill_gaps, polarity

def process_single_task(task_id: str, desc: str, notes: str) -> Dict:
    """Complete processing pipeline with enhanced output"""
    emotion, skill_gaps, sentiment_score = analyze_text_metadata(desc, notes)

    gaps_with_resources = []
    for skill in skill_gaps:
        resource = generate_tutorial(skill, desc)
        gaps_with_resources.append({
            "skill_gap": skill,
            "mini_tutorial": resource['tutorial'],
            "code_snippet": resource['code_snippet'],
            "resource_link": resource['resource_link'],
            "confidence": "High" if sentiment_score != 0 else "Medium"
        })

    return {
        "task_id": task_id,
        "task_description": desc[:100] + "..." if len(desc) > 100 else desc,
        "employee_notes": notes,
        "emotion_detected": emotion,
        "sentiment_score": round(sentiment_score, 2),
        "detected_skill_gaps": skill_gaps,
        "skill_gaps_with_resources": gaps_with_resources,
        "timestamp": datetime.now().isoformat(),
        "model_version": "Mistral-7B-v0.3-Structured-v5.0"
    }

