def extract_json_robust(response_text: str) -> Optional[Dict]:
    """
    Multi-strategy JSON extraction from LLM output.
    Handles markdown code blocks, extra text, and nested structures.
    """
    # Strategy 1: Find JSON in markdown code blocks
    code_block_pattern = r'```(?:json)?\s*([\s\S]*?)```'
    matches = re.findall(code_block_pattern, response_text)
    if matches:
        for match in matches:
            try:
                return json.loads(match.strip())
            except json.JSONDecodeError:
                continue

    # Strategy 2: Find outermost JSON object braces
    try:
        start = response_text.find('{')
        end = response_text.rfind('}')
        if start != -1 and end != -1 and end > start:
            return json.loads(response_text[start:end+1])
    except json.JSONDecodeError:
        pass

    # Strategy 3: Line-by-line JSON detection (for multiline)
    lines = response_text.split('\n')
    json_buffer = []
    in_json = False
    brace_count = 0

    for line in lines:
        if '{' in line and not in_json:
            in_json = True
            json_buffer = []

        if in_json:
            json_buffer.append(line)
            brace_count += line.count('{') - line.count('}')

            if brace_count == 0 and '{' in ''.join(json_buffer):
                try:
                    return json.loads('\n'.join(json_buffer))
                except json.JSONDecodeError:
                    continue

    return None

def detect_skill_gaps_llm(description: str, notes: str) -> List[str]:
    """
    Context-specific skill gap detection with strict validation.
    NO hardcoded fallbacks - forces LLM to reason from context.
    """

    prompt = f"""<s>[INST] You are a precise technical skill analyzer. Your task is to identify SPECIFIC skill gaps based on the provided task and employee notes.

TASK DESCRIPTION:
{description[:400]}

EMPLOYEE NOTES (Struggles/Confusions):
{notes[:300]}

ANALYSIS INSTRUCTIONS:
1. Identify exactly 2-3 specific technical concepts the employee is missing
2. Each skill gap must be:
   - A concrete technical term (e.g., "JWT token validation", "SQL query execution plans")
   - Derived DIRECTLY from the task description and employee notes
   - Specific enough to search for in documentation
3. DO NOT use generic terms like "Programming", "Development", "Technical Skills"
4. If the task mentions Flask → identify specific Flask concepts (blueprints, SQLAlchemy integration)
5. If the task mentions SQL → identify specific SQL concepts (indexing strategies, execution plans)

OUTPUT FORMAT (STRICT JSON):
{{
  "skill_gaps": [
    "specific technical concept 1",
    "specific technical concept 2"
  ],
  "reasoning": "brief explanation of why these gaps were identified"
}}

Be precise and context-specific. [/INST]</s>"""

    try:
        response = client.text_generation(
            prompt,
            max_new_tokens=200,
            temperature=0.15,  # Slightly higher for diversity
            stop_sequences=["</s>"]
        )

        parsed = extract_json_robust(response)

        if parsed and "skill_gaps" in parsed:
            gaps = parsed["skill_gaps"]
            # Validate: must be specific (multi-word) and context-relevant
            validated_gaps = []
            for gap in gaps:
                gap = gap.strip()
                # Must be more than 2 words and not generic
                if (len(gap.split()) >= 2 and
                    gap.lower() not in ["technical skill gap", "programming", "development"] and
                    len(gap) > 10):
                    validated_gaps.append(gap)

            if validated_gaps:
                return validated_gaps[:3]

        # If parsing fails, return empty to trigger context-aware fallback
        return []

    except Exception as e:
        print(f"⚠️ Skill detection error: {e}")
        return []

def get_contextual_fallback(task_desc: str, notes: str) -> List[str]:
    """
    Intelligent fallback based on keyword extraction from task description.
    Only used if LLM fails completely.
    """
    task_lower = task_desc.lower()
    notes_lower = notes.lower()

    # Technology-specific fallbacks
    if 'flask' in task_lower or 'sqlalchemy' in task_lower:
        return ["Flask blueprint architecture", "SQLAlchemy ORM session management"]
    elif 'sql' in task_lower or 'database' in task_lower or 'query' in task_lower:
        return ["SQL execution plan analysis", "Database indexing strategies"]
    elif 'react' in task_lower or 'frontend' in task_lower:
        return ["React hooks dependency management", "Component state optimization"]
    elif 'kubernetes' in task_lower or 'docker' in task_lower or 'ci/cd' in task_lower:
        return ["Kubernetes deployment configurations", "GitHub Actions workflow syntax"]
    elif 'python' in task_lower and ('excel' in task_lower or 'pdf' in task_lower):
        return ["Pandas data manipulation patterns", "ReportLab PDF generation"]
    else:
        return ["Core task-specific implementation patterns", "Relevant framework architecture"]

def generate_tutorial(skill_gap: str, task_context: str) -> Dict:
    """
    Generates specific learning resources with strict quality checks.
    """

    prompt = f"""<s>[INST] Create a precise learning resource for the following skill gap.

SKILL GAP TO ADDRESS: {skill_gap}
TASK CONTEXT: {task_context[:200]}

REQUIREMENTS:
1. Explanation: 2-3 sentences explaining the concept and WHY it matters for this specific task
2. Code: A 3-6 line working code example that directly demonstrates the skill
3. Resource: Official documentation URL only (no tutorials, no Medium articles)

QUALITY RULES:
- Code must be syntactically correct and runnable
- Explanation must connect to the task context
- No generic advice like "practice more" or "read docs"
- Be specific to the technology mentioned

OUTPUT FORMAT (STRICT JSON):
{{
  "tutorial": "Specific explanation tied to the task...",
  "code_snippet": "specific_code_example()",
  "resource_link": "https://official-docs.com/specific-page"
}}

Generate now: [/INST]</s>"""

    try:
        response = client.text_generation(
            prompt,
            max_new_tokens=350,
            temperature=0.2,
            stop_sequences=["</s>"]
        )

        parsed = extract_json_robust(response)

        if parsed and all(k in parsed for k in ["tutorial", "code_snippet", "resource_link"]):
            # Quality checks
            tutorial = parsed["tutorial"]
            code = parsed["code_snippet"]

            # Reject generic tutorials
            banned_phrases = ["learn through", "hands-on practice", "refer to", "documentation", "practice more"]
            if any(phrase in tutorial.lower() for phrase in banned_phrases):
                raise ValueError("Generic tutorial detected")

            # Code must be substantial
            if len(code.strip().split('\n')) < 2:
                raise ValueError("Code too short")

            return parsed

    except Exception as e:
        print(f"⚠️ Tutorial generation fallback for {skill_gap}: {e}")

    # Context-aware intelligent fallback
    return get_intelligent_fallback(skill_gap)

